package cn.ssx.Class;

public class SaleRecord {
	private String Sale_Productname;
	private String Sale_Productprice;
	private String Sale_date;
	private String Sale_Administrator;
	private String Sale_Productnumber;
	private String Sale_Profit;
	
	public String getSale_Productname() {
		return Sale_Productname;
	}

	public void setSale_Productname(String sale_Productname) {
		Sale_Productname = sale_Productname;
	}

	public String getSale_Productprice() {
		return Sale_Productprice;
	}

	public void setSale_Productprice(String sale_Productprice) {
		Sale_Productprice = sale_Productprice;
	}

	public String getSale_date() {
		return Sale_date;
	}

	public void setSale_date(String sale_date) {
		Sale_date = sale_date;
	}

	public String getSale_Administrator() {
		return Sale_Administrator;
	}

	public void setSale_Administrator(String sale_Administrator) {
		Sale_Administrator = sale_Administrator;
	}

	public String getSale_Productnumber() {
		return Sale_Productnumber;
	}

	public void setSale_Productnumber(String sale_Productnumber) {
		Sale_Productnumber = sale_Productnumber;
	}

	public String getSale_Profit() {
		return Sale_Profit;
	}

	public void setSale_Profit(String sale_Profit) {
		Sale_Profit = sale_Profit;
	}

	public SaleRecord() {
		super();
	}
	

}
