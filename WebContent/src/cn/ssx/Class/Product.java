package cn.ssx.Class;

public class Product {
	private String Product_name;
	private String Product_price;
	private int Product_number;
	private String Product_description;
	private String Product_storedate;
	private String Administrator_store;
	private String Storenumber;
	private String Product_prenumber;
	private String Product_honumber;
	
	


	public String getProduct_name() {
		return Product_name;
	}




	public void setProduct_name(String product_name) {
		Product_name = product_name;
	}




	public String getProduct_price() {
		return Product_price;
	}




	public void setProduct_price(String product_price) {
		Product_price = product_price;
	}




	public int getProduct_number() {
		return Product_number;
	}




	public void setProduct_number(int product_number) {
		Product_number = product_number;
	}




	public String getProduct_description() {
		return Product_description;
	}




	public void setProduct_description(String product_description) {
		Product_description = product_description;
	}




	public String getProduct_storedate() {
		return Product_storedate;
	}




	public void setProduct_storedate(String product_storedate) {
		Product_storedate = product_storedate;
	}




	public String getAdministrator_store() {
		return Administrator_store;
	}




	public void setAdministrator_store(String administrator_store) {
		Administrator_store = administrator_store;
	}




	public String getStorenumber() {
		return Storenumber;
	}




	public void setStorenumber(String storenumber) {
		Storenumber = storenumber;
	}




	public String getProduct_prenumber() {
		return Product_prenumber;
	}




	public void setProduct_prenumber(String product_prenumber) {
		Product_prenumber = product_prenumber;
	}




	public String getProduct_honumber() {
		return Product_honumber;
	}




	public void setProduct_honumber(String product_honumber) {
		Product_honumber = product_honumber;
	}




	public Product() {
		super();
	}
}
